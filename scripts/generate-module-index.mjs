/**
 * Scans shared module surfaces and generates docs/MODULE_INDEX.md
 * with a scannable list of all exports per file.
 *
 * Usage: node scripts/generate-module-index.mjs
 */

import { readdirSync, readFileSync, writeFileSync } from "fs";
import { join, basename } from "path";

const ROOT = new URL("..", import.meta.url).pathname.replace(/^\/([A-Z]:)/, "$1");

const SECTIONS = [
  { title: "Utilities", dir: "lib/utils", desc: "Pure utility functions" },
  { title: "Hooks", dir: "lib/hooks", desc: "TanStack Query hooks, mutation hooks, client state" },
  { title: "UI Components", dir: "components/ui", desc: "Layout and action primitives" },
  { title: "Form Components", dir: "components/form", desc: "Form controls (never use raw <input>/<select>)" },
  { title: "Constants", dir: "lib/constants", desc: "Game data definitions" },
];

const EXPORT_PATTERNS = [
  /^export\s+function\s+(\w+)/,
  /^export\s+const\s+(\w+)/,
  /^export\s+type\s+(\w+)/,
  /^export\s+interface\s+(\w+)/,
  /^export\s+\{\s*([^}]+)\}/,
  /^export\s+type\s+\{\s*([^}]+)\}/,
];

function extractExports(filePath) {
  const lines = readFileSync(filePath, "utf-8").split("\n");
  const names = [];
  for (const line of lines) {
    for (const pattern of EXPORT_PATTERNS) {
      const match = line.match(pattern);
      if (match) {
        // Patterns 5 & 6 capture comma-separated lists inside braces
        const raw = match[1];
        if (raw.includes(",") || pattern.source.includes("\\{")) {
          for (const token of raw.split(",")) {
            const name = token.trim().split(/\s+/)[0];
            if (name) names.push(name);
          }
        } else {
          names.push(raw);
        }
        break;
      }
    }
  }
  return [...new Set(names)];
}

function scanSection({ dir }) {
  const fullDir = join(ROOT, dir);
  let files;
  try {
    files = readdirSync(fullDir).filter((f) => /\.(ts|tsx)$/.test(f)).sort();
  } catch {
    return [];
  }
  return files.map((file) => ({
    file,
    exports: extractExports(join(fullDir, file)),
  }));
}

const lines = [
  "# Module Index",
  "",
  "Auto-generated by `node scripts/generate-module-index.mjs`. Do not edit manually.",
];

for (const section of SECTIONS) {
  const entries = scanSection(section);
  lines.push("", `## ${section.title}`, "", `\`${section.dir}/\` — ${section.desc}`, "");
  if (entries.length === 0) {
    lines.push("_(empty)_");
    continue;
  }
  for (const { file, exports } of entries) {
    const exportList = exports.length > 0 ? exports.join(", ") : "_(no named exports)_";
    lines.push(`- **${file}** — ${exportList}`);
  }
}

lines.push("");

const outPath = join(ROOT, "docs", "MODULE_INDEX.md");
writeFileSync(outPath, lines.join("\n"), "utf-8");
console.log(`Wrote ${outPath}`);
